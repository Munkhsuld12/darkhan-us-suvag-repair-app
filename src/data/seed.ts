  Array.from({ length: BAG_COUNT }, (_, bagIndex) => {
    const bagNo = bagIndex + 1;

    return Array.from({ length: STATIONS_PER_BAG }, (_, stationIndex) => {
      const stationNo = stationIndex + 1;
      return {
        id: `station-${bagNo}-${stationNo}`,
        code: `${bagNo}-${stationNo}`,
        name: "",
        bagNo,
        location: `Дархан сум, ${bagNo}-р баг, ${locationAnchors[stationIndex % locationAnchors.length]}`,
        caretakerName: caretakerNames[(bagIndex + stationIndex) % caretakerNames.length],
        caretakerPhone: `88${String(bagNo).padStart(2, "0")}${String(stationNo).padStart(4, "0")}`,
      };
    });
  }).flat();

